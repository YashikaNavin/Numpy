import numpy as np

# arange() function is used to create values by providing the start and end value as well the spacing between the values.
a=np.arange(1,10,2)  # print an array in which value starts from 1 and goes till 10 by having the space of 2 in between them
print(a)

b=np.arange(4,9)     # print an array which starts from 4 and goes till 9 with spacing of 1
print(b)

c=np.arange(20)      # print an array which starts from 0 and goes till one less than the passed argument
print(c)
